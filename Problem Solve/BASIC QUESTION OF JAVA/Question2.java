
public class Question2 {

    public static void printStar(int n){
        for(int i = 1; i<=n; i++){
            for(int j = 1; j<=n; j++){
                System.out.print("4"+" ");
            }
            System.out.println();
        }
    }
    public static void main(String[] args) {
        printStar(4);
        
    }
    
}
