public class javaDataType {
    public static void main(String[] args) {
        // byte datatype
        byte a = 8;
        System.out.println(a);
        
        // char datatype
        char ch = 'c';
        System.out.println(ch);

        // boolen datatype
        boolean var = false;
        System.out.println(var);

        // float datatype
       // float price = 8.5;

        // int datatype
        int number = 2;
        System.out.println(number);
    }
}
